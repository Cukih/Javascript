/**
 * Maior que: 
 * >
 * 
 * Menor que:
 * <
 * 
 * Maior ou igual:
 * >=
 * 
 * Menor ou igual:
 * <=
 * 
 * Igual:
 * == (pode ser variaveis diferentes com o mesmo resultado)
 * 
 * Diferente:
 * !=
 * 
 * Idêntico:
 * === (tem que ser a mesma variavel e mesmo resultado)
 */

console.log(1 > 2);
console.log(5 < 10);
console.log(3 >= 3);
console.log(5 <= 4);
console.log(5 == 4);s
console.log('Thiago' != 'Arcanjo'); //true, pois Thiago é diferente de Arcanjo
console.log(3 === '3'); //false, pois É IGUAL mas NÃO é IDÊNTICO
console.log(3 == '3'); //true, pois é igual
console.log(4 === 4); //true, pois é igual e idêntico